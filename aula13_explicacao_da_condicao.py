# Se / Se não se / Se não

condicao = input('Informe a condição: ')

if condicao == True:
    print('Condição verdadeira') 
elif condicao == False:
    print('Condição falsa')
elif condicao == '"A" ou "a"':
    print('Condição não condizente')
else:
    print('Tente novamente')